package verhovnaRada;

public class Human {
	
	private int wight;
	private	 int height;
	
	public Human(int wight, int height) {
		super();
		this.wight = wight;
		this.height = height;
	}

	public int getWight() {
		return wight;
	}

	public void setWight(int wight) {
		this.wight = wight;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}
	
	
	
}
